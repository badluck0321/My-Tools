/* ─── PhotoImage: Renders a single photo using the full URL ── */
const PhotoImage = ({ photoUrl, alt, className, emptyLabel }) => {
  if (!photoUrl) {
    return (
      <div
        className={`${className} bg-[#f0eeeb] dark:bg-[#2d2a27] flex items-center justify-center text-[#8a8580] text-sm`}>
        {emptyLabel}
      </div>
    );
  }

  return (
    <img
      src={photoUrl} // This is now the FULL URL generated by your backend
      alt={alt}
      className={className}
      loading="lazy"
      onLoad={() => console.log("Loaded:", photoUrl)}
      onError={(e) => {
        console.error("Failed image:", photoUrl);
        e.target.src = "/placeholder.jpg";
      }}
    />
  );
};

/* ─── Image Gallery ───────────────────────────────── */
const Gallery = ({ photoUrls, photoEmptyLabel, galleryEmptyLabel }) => {
  const [current, setCurrent] = useState(0);
  const total = photoUrls ? photoUrls.length : 0; // Safety check for null/undefined

  // If there are no photos, show a clean placeholder
  if (total === 0) {
    return (
      <div className="relative overflow-hidden rounded-2xl bg-[#f0eeeb] dark:bg-[#2d2a27] aspect-[4/3] flex items-center justify-center text-[#8a8580]">
        {galleryEmptyLabel}
      </div>
    );
  }

  const prev = () => setCurrent((c) => (c - 1 + total) % total);
  const next = () => setCurrent((c) => (c + 1) % total);

  return (
    <div className="relative w-full">
      {/* Main image */}
      <div className="relative overflow-hidden rounded-2xl bg-[#f0eeeb] dark:bg-[#2d2a27] aspect-[4/3]">
        <AnimatePresence mode="wait">
          <motion.div
            key={current}
            initial={{ opacity: 0, x: 40 }}
            animate={{ opacity: 1, x: 0 }}
            exit={{ opacity: 0, x: -40 }}
            transition={{ duration: 0.3 }}
            className="w-full h-full">
            <PhotoImage
              photoUrl={photoUrls[current]}
              alt={`Photo ${current + 1}`}
              className="w-full h-full object-cover"
              emptyLabel={photoEmptyLabel}
            />
          </motion.div>
        </AnimatePresence>

        {/* Nav arrows — only if multiple photos */}
        {total > 1 && (
          <>
            <button
              onClick={prev}
              className="absolute left-3 top-1/2 -translate-y-1/2 bg-white/80 dark:bg-black/50 backdrop-blur-sm p-2 rounded-full shadow hover:scale-110 transition-transform">
              <ChevronLeft size={20} />
            </button>
            <button
              onClick={next}
              className="absolute right-3 top-1/2 -translate-y-1/2 bg-white/80 dark:bg-black/50 backdrop-blur-sm p-2 rounded-full shadow hover:scale-110 transition-transform">
              <ChevronRight size={20} />
            </button>

            {/* Counter badge */}
            <span className="absolute bottom-3 right-3 bg-black/50 text-white text-xs px-2.5 py-1 rounded-full backdrop-blur-sm">
              {current + 1} / {total}
            </span>
          </>
        )}
      </div>

      {/* Thumbnails — only if multiple photos */}
      {total > 1 && (
        <div className="flex gap-2 mt-3 overflow-x-auto pb-1">
          {/* Fixed: Changed 'id' to 'url' so it correctly passes the full URL */}
          {photoUrls.map((url, i) => (
            <button
              key={url}
              onClick={() => setCurrent(i)}
              className={`flex-shrink-0 w-16 h-16 rounded-xl overflow-hidden border-2 transition-all ${
                i === current
                  ? "border-[#6d2842] scale-105"
                  : "border-transparent opacity-60 hover:opacity-100"
              }`}>
              <PhotoImage
                photoUrl={url}
                alt={`Thumb ${i + 1}`}
                className="w-full h-full object-cover"
                emptyLabel={photoEmptyLabel}
              />
            </button>
          ))}
        </div>
      )}
    </div>
  );
};

