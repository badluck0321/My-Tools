/* Main Component */
/* ───────────────────────────────────────────── */
const MasteryInfos = () => {
  const { id } = useParams();
  const navigate = useNavigate();
  const { t } = useTranslation();

  const [mastery, setMastery] = useState(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);
  const [isLiked, setIsLiked] = useState(false);
  const [favLoading, setFavLoading] = useState(false);
  const { authenticated, login } = useKeycloak();
  const { lookups } = useLookups();

  useEffect(() => {
    setLoading(true);
    masteryService
      .getMasteryById(id)
      .then(setMastery)
      .catch((err) =>
        setError(err?.message ?? t("masteryInfo.failedToLoad"))
      )
      .finally(() => setLoading(false));
  }, [id]);

  useEffect(() => {
    if (!authenticated || !id) return;
    favoriteService
      .statusMastery(id)
      .then((res) => setIsLiked(Boolean(res.data?.favorited)))
      .catch(() => setIsLiked(false));
  }, [authenticated, id]);

  const handleToggleFavorite = async () => {
    if (!authenticated) {
      login();
      return;
    }
    if (favLoading) return;
    setFavLoading(true);
    try {
      const res = await favoriteService.toggleMastery(mastery.id);
      setIsLiked(Boolean(res.data?.favorited));
    } finally {
      setFavLoading(false);
    }
  };

  if (loading) return <Loading text={t("masteryInfo.loading")} />;

  if (error) {
    return (
      <div className="min-h-screen flex flex-col items-center justify-center gap-4">
        <p className="text-[#6d2842] font-medium">{error}</p>
        <button
          onClick={() => navigate(-1)}
          className="flex items-center gap-2 text-sm underline">
          <ArrowLeft size={16} /> {t("common.goBack")}
        </button>
      </div>
    );
  }

  const typeLabel = lookupLabel(
    lookups,
    LOOKUP_TYPES.MASTERY_TYPE,
    mastery.typeId ?? mastery.masteryTypeId,
    "Service"
  );
  const statusLabel = lookupLabel(
    lookups,
    LOOKUP_TYPES.MASTERY_STATUS,
    mastery.masteryStatuId,
    mastery.masteryStatuId || "—"
  );
  const pricingLabel = lookupLabel(
    lookups,
    LOOKUP_TYPES.PRICING_TYPE,
    mastery.pricingType,
    mastery.pricingType || "—"
  );

  return (
    <div className="min-h-screen bg-gradient-to-br from-[#fafaf9] via-[#f5f5f3] to-[#e8e7e5] dark:from-[#1a1816] dark:via-[#2d2a27] dark:to-[#3a3633]">
      <div className="container-custom py-12">
        {/* Back Button */}
        <motion.button
          initial={{ opacity: 0, x: -10 }}
          animate={{ opacity: 1, x: 0 }}
          onClick={() => navigate(-1)}
          className="flex items-center gap-2 text-sm text-[#5d5955] dark:text-[#c4bfb9] hover:text-[#6d2842] dark:hover:text-[#e8a0b4] transition-colors mb-8 group">
          <ArrowLeft
            size={16}
            className="group-hover:-translate-x-1 transition-transform"
          />
          {t("masteryInfo.backToMasteries")}
        </motion.button>

        <div className="grid grid-cols-1 lg:grid-cols-2 gap-12">
          {/* Left Side Image - FIXED: Now uses the Gallery component */}
          <motion.div
            initial={{ opacity: 0, y: 20 }}
            animate={{ opacity: 1, y: 0 }}>
            <Gallery
              photoUrls={mastery.photoUrls || []}
              photoEmptyLabel={t("masteryInfo.noImageAvailable")}
              galleryEmptyLabel={t("masteryInfo.noImagesAvailable")}
            />
          </motion.div>

          {/* Right Side Content */}
          <motion.div
            initial={{ opacity: 0, y: 20 }}
            animate={{ opacity: 1, y: 0 }}
            transition={{ delay: 0.1 }}
            className="flex flex-col gap-6">
            {/* Badges */}
            <div className="flex flex-wrap gap-2">
              <Badge color="green">
                <CheckCircle size={12} />
                {statusLabel}
              </Badge>
              <Badge color="primary">
                <Briefcase size={12} />
                {pricingLabel}
              </Badge>
            </div>
            {/* Title */}
            <div>
              <h1 className="text-4xl md:text-5xl font-display font-bold mb-3 text-[#1a1816] dark:text-[#f0ece8]">
                {mastery.title}
              </h1>
              <p className="text-3xl font-bold bg-gradient-to-r from-[#6d2842] via-[#8b3654] to-[#a64d6d] bg-clip-text text-transparent">
                {mastery.price} MAD
              </p>
            </div>
            {/* Description */}
            <div className="glass dark:glass-dark rounded-2xl p-5">
              <h3 className="font-semibold text-lg mb-3">{t("masteryInfo.about")}</h3>
              <p className="text-[#5d5955] dark:text-[#c4bfb9] leading-relaxed">
                {mastery.description}
              </p>
            </div>
            {/* Master Information */}
            <div className="glass dark:glass-dark rounded-2xl p-5">
              <h3 className="font-semibold text-lg mb-4">{t("masteryInfo.masterInformation")}</h3>
              <div className="flex justify-between items-center py-3 border-b border-[#e8e7e5] dark:border-[#3a3633] last:border-0">
                <span className="text-sm text-[#8a8580] dark:text-[#7a756f]">
                  {t("masteryInfo.master")}
                </span>
                <button
                  type="button"
                  onClick={() =>
                    navigate(`/Masterys/history/${mastery.masterId}`)
                  }
                  className="text-sm font-medium text-[#6d2842] dark:text-[#a4c9b3] underline">
                  {mastery.masterName}
                </button>
              </div>
              <InfoRow label={t("masteryInfo.masterId")} value={mastery.masterId} />
              <InfoRow label={t("masteryInfo.phone")} value={mastery.masterPhone} />
              <InfoRow label={t("masteryInfo.city")} value={mastery.city} />
              <InfoRow
                label={t("masteryInfo.experience")}
                value={`${mastery.experienceYears} ${t("masteryInfo.years")}`}
              />
              <InfoRow label={t("masteryInfo.pricingType")} value={pricingLabel} />
              <InfoRow label={t("masteryInfo.status")} value={statusLabel} />
            </div>
            {/* Highlights */}
            <div className="grid grid-cols-2 gap-4">
              <div className="glass dark:glass-dark rounded-xl p-4 text-center">
                <Award size={22} className="mx-auto mb-2 text-[#6d2842]" />
                <p className="text-lg font-bold">{mastery.experienceYears}</p>
                <p className="text-xs text-[#8a8580]">{t("masteryInfo.yearsExperience")}</p>
              </div>
              <div className="glass dark:glass-dark rounded-xl p-4 text-center">
                <MapPin size={22} className="mx-auto mb-2 text-[#6d2842]" />
                <p className="text-lg font-bold">{mastery.city}</p>
                <p className="text-xs text-[#8a8580]">{t("masteryInfo.serviceLocation")}</p>
              </div>
            </div>

            <BookingCalendar resourceType="MASTERY" resourceId={mastery.id} title={t("masteryInfo.bookingCalendar")} quantityEnabled={false} />

            {/* Actions */}

            <ContactButton
              resourceType="MASTERY" // or "MASTERY" or "DEMANDE"
              resourceId={mastery.id}
              initialMessage={`Bonjour, je suis intéressé(e) par votre service de ${mastery.title}.`}
            />
            <div className="flex gap-3 mt-auto">
              <a
                href={`tel:${mastery.masterPhone}`}
                className="flex-1 flex items-center justify-center gap-2 bg-gradient-to-r from-[#6d2842] via-[#8b3654] to-[#a64d6d] text-white font-semibold py-3.5 rounded-xl hover:opacity-90 active:scale-95 transition-all shadow-lg shadow-[#6d2842]/20">
                <Phone size={18} /> {t("masteryInfo.contactMaster")}
              </a>
              <button
                onClick={handleToggleFavorite}
                disabled={favLoading}
                className={`p-3.5 rounded-xl border transition-all ${
                  isLiked
                    ? "bg-[#6d2842] border-[#6d2842] text-white"
                    : "border-[#d4cfc9] dark:border-[#4a4642] text-[#5d5955] dark:text-[#c4bfb9] hover:border-[#6d2842] hover:text-[#6d2842]"
                }`}>
                <Heart size={18} fill={isLiked ? "currentColor" : "none"} />
              </button>
              <button className="p-3.5 rounded-xl border border-[#d4cfc9] dark:border-[#4a4642] text-[#5d5955] dark:text-[#c4bfb9] hover:border-[#6d2842] hover:text-[#6d2842] transition-all">
                <Share2 size={18} />
              </button>
            </div>
          </motion.div>
        </div>
      </div>
    </div>
  );
};

export default MasteryInfos;
